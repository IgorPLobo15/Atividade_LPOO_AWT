    public static boolean temNumerosRepetidos(int[] array) {
        Arrays.sort(array); // Classifica o array para que números iguais estejam adjacentes
        for (int i = 1; i < array.length; i++) {
            if (array[i] == array[i - 1]) {
                return true; // Encontrou um número repetido
            }
        }
        return false; // Não encontrou números repetidos
    }

