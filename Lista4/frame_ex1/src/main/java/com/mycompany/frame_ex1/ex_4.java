    private void OrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OrdenarActionPerformed
        int num [] = new int [5];
        
        num[0]=Integer.valueOf(jnum1.getText());
        num[1]=Integer.valueOf(jnum2.getText());
        num[2]=Integer.valueOf(jnum3.getText());
        num[3]=Integer.valueOf(jnum4.getText());
        num[4]=Integer.valueOf(jnum5.getText());       

        if (temNumerosRepetidos(num)) {
            JOptionPane.showMessageDialog(null,"Aviso: Não coloque números repetidos no array.");          
        } else {            
            final int n = num.length; //tamanho do vetor
        int aux; //variavel auxiliar de troca
            for (int i = 0; i < n-1; i++){
                for (int j = 0; j < n-1-i; j++){
                    if (num[j] > num[j+1]){
                    aux = num[j];
                    num[j] = num[j+1];
                    num[j+1] = aux;
                    }
                }
            }
            String numerosString = "Números no array:\n";

        // Itere pelo array e adicione cada número à String
        for (int i = 0; i < num.length; i++) {
            numerosString += num[i] + "\n";
        }

        // Mostre a String com os números usando JOptionPane
        JOptionPane.showMessageDialog(null, numerosString);
        }
        
        
    }//GEN-LAST:event_OrdenarActionPerformed

