    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
        int inicial = Integer.valueOf(jTextField1.getText().replaceAll(",","."));
        int fim = Integer.valueOf(jTextField2.getText().replaceAll(",","."));
        int auto = fim- inicial;
        double litros = Double.valueOf(jTextField3.getText().replaceAll(",", "."));
            if(auto<=0){
                JOptionPane.showMessageDialog(null,"A kilometragem atual deve ser \n maior do que a anterior","Erro",0);
                return ;
                }
        
        double rend = auto/litros;
        JOptionPane.showMessageDialog(null,"O consumo foi de :"+" "+rend+" Km / L\n a autonomia foi de "+
        auto+" Kilometros","Resultado",-1);                    
        
    }//GEN-LAST:event_CalcularActionPerformed

