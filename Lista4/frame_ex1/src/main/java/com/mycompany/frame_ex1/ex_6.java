    private void calcularButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularButtonActionPerformed
         try {
                    // Obtém os valores dos campos de texto e converte para inteiros
                    int primeiroDividendo = Integer.parseInt(primeiroDividendoTextField.getText());
                    int ultimoDividendo = Integer.parseInt(ultimoDividendoTextField.getText());
                    int primeiroDivisor = Integer.parseInt(primeiroDivisorTextField.getText());
                    int ultimoDivisor = Integer.parseInt(ultimoDivisorTextField.getText());

                    // Realiza o cálculo da divisão e exibe o resultado
                    StringBuilder resultado = new StringBuilder();
                    for (int dividendo = primeiroDividendo; dividendo <= ultimoDividendo; dividendo++) {
                        for (int divisor = primeiroDivisor; divisor <= ultimoDivisor; divisor++) {
                            if (divisor != 0) {
                                int resultadoDivisao = dividendo / divisor;
                                resultado.append("Dividendo: ").append(dividendo)
                                        .append(", Divisor: ").append(divisor)
                                        .append(", Resultado: ").append(resultadoDivisao)
                                        .append("\n");
                            }
                        }
                    }

                    // Exibe o resultado em uma caixa de diálogo
                    JOptionPane.showMessageDialog(null, "Resultados da divisão:\n" + resultado.toString());
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null, "Por favor, insira valores numéricos válidos.");
                }
    }//GEN-LAST:event_calcularButtonActionPerformed

